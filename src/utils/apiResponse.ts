import { Response } from "express";

export function success<T>(res: Response, data: T, status: number = 200): Response {
  return res.status(status).json({
    success: true,
    data,
  });
}

export function error(
  res: Response,
  message: string = "Errore interno",
  status: number = 500,
): Response {
  return res.status(status).json({
    success: false,
    error: message,
  });
}
